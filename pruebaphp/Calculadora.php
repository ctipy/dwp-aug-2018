<?php 
class Calculadora 
{

    public function sumar($valor1, $valor2){

        return $valor1 + $valor2;

    }

    public function restar($valor1, $valor2)
    {

        return $valor1 -$valor2;

    }

    public function dividir($valor1, $valor2)
    {

        return $valor1 / $valor2;

    }


}


?>